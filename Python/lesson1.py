'''
+ Addition
- Subtraction
* Multiplication
/ Division
% Mod (the remainder after dividing)
** Exponentiation (note that ^ does not do this operation, as you might have seen in other languages)
// Divides and rounds down to the nearest integer
'''
print((1+2)*3**3)
print(3 + 5) 
print(1 + 2 + 3 * 3) 
print(3 ** 2)
print(9 % 2)